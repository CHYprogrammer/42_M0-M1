#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t			i;
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	i = 0;
	while (i < n)
	{
		if (s >= d)
			d[i] = s[i];
		else
			d[n - i - 1] = s[n - i - 1];
		i++;
	}
	return (dest);
}
